//Pattern programming 1
//Write a java program to display the pattern
//* * * *
//* * * *
//* * * *
//* * * *
public class pattern_programming_one {

	public static void main(String[] args) {

		for (int j = 0; j < 4; j++) {
			for (int i = 0; i < 4; i++) {
				System.out.print("* ");

			}
			System.out.println(" ");
		}

	}

}
