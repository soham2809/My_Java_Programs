public class Arithmetic_operators {

	public static void main(String[] args) {
		int num1 = 100;
		int num2 = 20;
		// Use of +/addition operator
		System.out.println(num1 + num2);
		// Use of -/subtraction operator
		System.out.println(num1 - num2);
		// Use of Division// operators
		System.out.println(num1 / num2);
		// Use of Modulo/ % operator
		System.out.println(num1 % num2);

	}

}
