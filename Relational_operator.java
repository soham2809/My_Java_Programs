
public class Relational_operator {

	public static void main(String[] args) {
		int a, b, c, d;
		a = 10;
		b = 20;
		c = 30;
		d = 40;
		boolean result = a <= b;
		System.out.println(result);
		// or you can write
		System.out.println(a <= b);
		System.out.println(a == b);
		System.out.println(a < b);
		System.out.println(a > b);
		System.out.println(c >= d);
		System.out.println(a != d);
		System.out.println(!true);

	}

}
