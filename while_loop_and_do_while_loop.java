
public class while_loop_and_do_while_loop {

	public static void main(String[] args) {
		// WHILE LOOP
		int i = 0;
		while (i < 9) {
			System.out.print("*");
			System.out.print(" ");
			i++;
		}
		// DO-WHILE LOOP
		int j = 0;
		do {
			System.out.println("*");
			j++;
		} while (j < 8);

	}

}
